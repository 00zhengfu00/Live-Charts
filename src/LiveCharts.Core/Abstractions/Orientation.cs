namespace LiveCharts.Core.Abstractions
{
    /// <summary>
    /// The Orientation.
    /// </summary>
    public enum Orientation
    {
        /// <summary>
        /// Tells the chart to decide automatically the orientation based on the data it has.
        /// </summary>
        Auto,
        /// <summary>
        /// Sets a vertical orientation.
        /// </summary>
        Vertical,
        /// <summary>
        /// Sets an horizontal orientation.
        /// </summary>
        Horizontal
    }
}