namespace LiveCharts.Core.Dimensions
{
    /// <summary>
    /// Represents a range.
    /// </summary>
    public class DataRange
    {
        /// <summary>
        /// Gets or sets from.
        /// </summary>
        /// <value>
        /// From.
        /// </value>
        public double MaxValue { get; set; }

        /// <summary>
        /// Gets or sets to.
        /// </summary>
        /// <value>
        /// To.
        /// </value>
        public double MinValue { get; set; }
    }
}
