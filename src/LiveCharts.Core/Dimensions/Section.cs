namespace LiveCharts.Core.Dimensions
{
    /// <summary>
    /// Defines a section in an axis.
    /// </summary>
    public class Section
    {
        /// <summary>
        /// Gets or sets the value.
        /// </summary>
        /// <value>
        /// The value.
        /// </value>
        public double Value { get; set; }

        /// <summary>
        /// Gets or sets the width.
        /// </summary>
        /// <value>
        /// The width.
        /// </value>
        public double Width { get; set; }
    }
}